// 一只青蛙一次可以跳上 1 级台阶，也可以跳上2 级。求该青蛙跳上一个n 级的台阶总共有多少种跳法。

// 输入格式:
// 首先输入数字n，代表接下来有n组输入，50>=n>=0,然后每行一个数字，代表台阶数，数字为小于60的整数

// 输出格式:
// 对每一组输入，输出青蛙的跳法。

#include <stdio.h>

int main() {
    int n, i, j, m;
    int dp[60] = {0};
    dp[0] = 1;
    dp[1] = 1;
    dp[2] = 2;
    scanf("%d", &n);
    for (i = 0; i < n; i++) {
        scanf("%d", &m);
        for (j = 3; j <= m; j++) {
            dp[j] = dp[j-1] + dp[j-2];
        }
        printf("%d\n", dp[m]);
    }
    return 0;
}